import { NextRequest, NextResponse } from 'next/server'
import { requireAdmin } from '@/lib/admin/require-admin'
import { auditLog } from '@/lib/admin/audit'
import { prisma } from '@/lib/prisma'
import { createPromotionAdminSchema } from '@/lib/validations/admin'
import { rateLimit, getIP, createRateLimitKey } from '@/lib/rate-limit'
import { logError } from '@/lib/logger'

// GET - Liste toutes les promotions
export async function GET(req: NextRequest) {
  const rl = await rateLimit(createRateLimitKey(getIP(req), 'admin:promotions'), { windowMs: 60_000, maxRequests: 30 })
  if (!rl.success) return NextResponse.json({ error: 'Zu viele Anfragen' }, { status: 429 })
  try {
    const authz = await requireAdmin()
    if (!authz.ok) return NextResponse.json({ error: 'Unauthorized' }, { status: authz.status })

    const promotions = await prisma.promotion.findMany({
      orderBy: { createdAt: 'desc' }
    })

    return NextResponse.json(promotions)
  } catch (error) {
    logError('Error fetching promotions:', error)
    return NextResponse.json(
      { error: 'Failed to fetch promotions' },
      { status: 500 }
    )
  }
}

// POST - Créer une nouvelle promotion
export async function POST(request: NextRequest) {
  const rl = await rateLimit(createRateLimitKey(getIP(request), 'admin:promotions:post'), { windowMs: 60_000, maxRequests: 15 })
  if (!rl.success) return NextResponse.json({ error: 'Zu viele Anfragen' }, { status: 429 })
  try {
    const authz = await requireAdmin()
    if (!authz.ok) return NextResponse.json({ error: 'Unauthorized' }, { status: authz.status })

    const body = await request.json()
    const parsed = createPromotionAdminSchema.safeParse(body)
    if (!parsed.success) {
      return NextResponse.json(
        { error: 'Ungültige Daten', details: parsed.error.flatten().fieldErrors },
        { status: 400 }
      )
    }

    const data = parsed.data
    const promotion = await prisma.promotion.create({
      data: {
        ...data,
        startDate: new Date(data.startDate),
        endDate: new Date(data.endDate),
        autoGenerated: false,
      }
    })

    await auditLog({
      action: 'CREATE',
      entityType: 'Promotion',
      entityId: promotion.id,
      userId: authz.session.user.id,
      newValues: promotion,
      ipAddress: request.headers.get('x-forwarded-for'),
      userAgent: request.headers.get('user-agent'),
    })

    return NextResponse.json(promotion)
  } catch (error) {
    logError('Error creating promotion:', error)
    return NextResponse.json(
      { error: 'Failed to create promotion' },
      { status: 500 }
    )
  }
}
